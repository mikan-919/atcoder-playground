import fs from 'node:fs';

// ================================ ================================ ================================
// ================================ ===== ここからセットアップ ===== ================================
// ================================ ================================ ================================
function inputs() {
    const input = fs.readFileSync("/dev/stdin", "utf8");
    const lines = input.split("\n");
    return lines;
}
const print = (...args) => console.log(...args);
// ================================ ================================ ================================
// ================================ ===== ここまでセットアップ ===== ================================
// ================================ ================================ ================================
function main() {
    const lines = inputs();
    print(lines[0].slice(0, -1));
}
main();
